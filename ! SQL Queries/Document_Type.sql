SELECT 
   
  dt.DOC_TYPE_DESC,
  dt.IS_ACTIVE,
  oit.ORDER_ITEM_TYPE_DISPLAY_NAME,
  oitdt.IS_ACTIVE,
  oitdt.IS_DEFAULT
FROM VG69999.ORDER_ITEM_TYPE_DOC_TYPE oitdt
join VG69999.doc_type dt on oitdt.DOC_TYPE_ID_DOC = dt.doc_type_id
JOIN VG69999.ORDER_ITEM_TYPE oit ON oit.ORDER_ITEM_TYPE_ID = oitdt.ORDER_ITEM_TYPE_ID
WHERE --dt.DOC_TYPE_DESC = 'ABANDONMENT OF ASSUMED NAME'
      oit.ORDER_ITEM_TYPE_ID = 1
	  AND oitdt.IS_DEFAULT = 1
ORDER BY oit.ORDER_ITEM_TYPE_DISPLAY_NAME ASC


