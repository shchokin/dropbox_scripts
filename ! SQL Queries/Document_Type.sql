SELECT 
        oit.ORDER_ITEM_TYPE_ID, 
		oit.ORDER_ITEM_TYPE_DISPLAY_NAME AS 'Document Type Available at Order Entry ', 
		oit_dt.IS_ACTIVE, 
		oit_dt.IS_DEFAULT,
		st.SERVICE_TYPE_DESC
FROM VG69999.DOCGROUP_DOCTYPE dg_dt
JOIN VG69999.DOC_TYPE dt ON dg_dt.DOC_TYPE_ID = dt.DOC_TYPE_ID
JOIN VG69999.DOC_GROUP dg ON dg_dt.DOC_GROUP_ID = dg.DOC_GROUP_ID
JOIN VG69999.WORKFLOW wf ON dg.DOC_GROUP_ID = wf.DOC_GROUP_ID
JOIN VG69999.ORDER_ITEM_TYPE oit ON wf.ORDER_ITEM_TYPE_ID = oit.ORDER_ITEM_TYPE_ID
JOIN VG69999.TENANT_SERVICE_TYPE tst ON wf.TENANT_SERVICE_TYPE_ID = tst.TENANT_SERVICE_TYPE_ID
JOIN VANGUARD.SERVICE_TYPE st ON tst.SERVICE_TYPE_ID = st.SERVICE_TYPE_ID
JOIN VG69999.TENANT_ORIGIN t_o ON wf.TENANT_ORIGIN_ID = t_o.TENANT_ORIGIN_ID
JOIN VANGUARD.ORIGIN o ON t_o.ORIGIN_ID = o.ORIGIN_ID
LEFT JOIN VG69999.ORDER_ITEM_TYPE_DOC_TYPE oit_dt ON oit.ORDER_ITEM_TYPE_ID = oit_dt.ORDER_ITEM_TYPE_ID AND dt.DOC_TYPE_ID = oit_dt.DOC_TYPE_ID_DOC
WHERE st.SERVICE_TYPE_DESC IN ('Recording', 'eFile', 'Indexing', 'Capture', 'Marriage', 'Filings')
AND o.ORIGIN_ID NOT IN (5, 20)
AND dt.DOC_TYPE_DESC = 'ABANDONMENT OF ASSUMED NAME' --AND oit.ORDER_ITEM_TYPE_ID IN (1, 240)
ORDER BY oit.ORDER_ITEM_TYPE_DISPLAY_NAME 


